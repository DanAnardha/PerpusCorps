    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jDateChooser2.getDate() == null || jDateChooser3.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Silikan pilih tanggal terlebih dahulu!");
        } else {
            String bulan = "yyyy-MM-dd";
            String tahun = "yyyy-MM-dd";
            SimpleDateFormat fm = new SimpleDateFormat(bulan);
            SimpleDateFormat fl = new SimpleDateFormat(tahun);
            String bulanan = String.valueOf(fm.format(jDateChooser2.getDate()));
            String tahunan = String.valueOf(fl.format(jDateChooser3.getDate()));
            loadReport("src\\Laporan\\LaporanPinjamPeriodik.jrxml", bulanan, tahunan);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

