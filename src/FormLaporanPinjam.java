    public void loadReport(String lapor, String tanggal, String tahun) {
        try {
            HashMap parameter = new HashMap();
            parameter.put("harian", tanggal);
            parameter.put("bulan", tanggal);
            parameter.put("tahun", tahun);
            jPanel5.removeAll();
            jPanel5.repaint();
            jPanel5.revalidate();
            JasperDesign jdesign = JRXmlLoader.load(lapor);
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            JasperPrint jprint = JasperFillManager.fillReport(jreport, parameter, koneksiDB);

            JRViewer v = new JRViewer(jprint);
            jPanel5.setLayout(new BorderLayout());
            jPanel5.add(v);
        } catch (JRException ex) {

        }
    }

