    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pastikan tidak ada field yang kosong!");
        } else {
            try {
                PreparedStatement stm = koneksiDB.prepareStatement("select * from kategori where id_kategori='" + jTextField1.getText() + "'");
                //stm.executeUpdate();
                ResultSet rs = stm.executeQuery();
                if (!rs.next()) {
                    PreparedStatement stmt = koneksiDB.prepareStatement("insert into kategori (id_kategori, kategori)"
                            + "values ('" + jTextField1.getText() + "','" + jTextField2.getText() + "')");
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Simpan data berhasil!");

                    kondisiAwal();
                    idOtomatis();
                } else {
                    PreparedStatement stmt = koneksiDB.prepareStatement("update kategori set kateogri='" + jTextField2.getText() + "'");
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Update data berhasil!");
                    kondisiAwal();
                    idOtomatis();
                }

            } catch (HeadlessException | SQLException e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

