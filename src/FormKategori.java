    private void kondisiAwal(){
        jTextField2.setText("");
        try {
            jTextField2.setText("");
            ResultSet hasil = koneksiDB.createStatement().executeQuery("select * from kategori");
            DefaultTableModel model = (DefaultTableModel) TBLKategori.getModel();
            model.setRowCount(0);
            while (hasil.next()) {
                model.addRow(new String[]{hasil.getString(1), hasil.getString(2)});
            }
        } catch (SQLException ex) {
            System.out.println("Error : " + ex.getMessage());
        }
    }
    
    private void idOtomatis() {
        try {
            ResultSet hasil = koneksiDB.createStatement().executeQuery("select id_kategori from kategori order by id_kategori desc limit 1");
            if (hasil.next()) {
                String rnno = hasil.getString("id_kategori");
                int co = rnno.length();
                String txt = rnno.substring(0, 3);
                String num = rnno.substring(3, 7);
                int n = Integer.parseInt(num);
                n++;
                String snum = Integer.toString(n);
                String ftxt = txt + snum;
                jTextField1.setText(ftxt);
            } else {
                jTextField1.setText("KTG1000");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormPetugas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

