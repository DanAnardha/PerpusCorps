    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PreparedStatement stm = koneksiDB.prepareStatement("select * from kategori where id_kategori='" + jTextField1.getText() + "'");
            //stm.executeUpdate();
            ResultSet rs = stm.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Pilih record yang ingin di hapus!!");
            } else {
                PreparedStatement stmt = koneksiDB.prepareStatement("delete from kategori where id_kategori='" + jTextField1.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Hapus data berhasil!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormPetugas.class.getName()).log(Level.SEVERE, null, ex);
        }
        kondisiAwal();
        idOtomatis();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void TBLKategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLKategoriMouseClicked
        try {
            int row = TBLKategori.getSelectedRow();
            String to = TBLKategori.getModel().getValueAt(row, 0).toString();
            ResultSet hasil = koneksiDB.createStatement().executeQuery("select * from kategori where id_kategori ='" + to + "'");
            if (hasil.next()) {
                String id = hasil.getString(1);
                String kategori = hasil.getString(2);
                jTextField1.setText("" + id);
                jTextField2.setText(kategori);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormPetugas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TBLKategoriMouseClicked

