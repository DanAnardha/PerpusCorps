    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pastikan tidak ada data yang kosong!");
        } else {
            try {
                PreparedStatement stmt = koneksiDB.prepareStatement("update lembaga set nama_lembaga='" + jTextField2.getText() + "', alamat='" + jTextField3.getText() + "', telepon='" + jTextField4.getText() + "', email='" + jTextField5.getText() + "', website='" + jTextField6.getText() + "' where id_lembaga='" + jTextField1.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Simpan data berhasil!");
                kondisiAwal();
            } catch (HeadlessException | SQLException e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

