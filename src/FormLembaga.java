    private void kondisiAwal() {
        try {
            ResultSet hasil = koneksiDB.createStatement().executeQuery("select * from lembaga where id_lembaga ='LBG1000'");
            if (hasil.next()) {
                String id = hasil.getString(1);
                jTextField1.setText(id);
                jTextField2.setText(hasil.getString(2));
                jTextField3.setText(hasil.getString(3));
                jTextField4.setText(hasil.getString(4));
                jTextField5.setText(hasil.getString(5));
                jTextField6.setText(hasil.getString(6));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormLembaga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

