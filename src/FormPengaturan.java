    private void kondisiAwal() {
        try {
            ResultSet hasil = koneksiDB.createStatement().executeQuery("select * from pengaturan where id_pengaturan ='PGT1001'");
            if (hasil.next()) {
                String id = hasil.getString(1);
                jTextField6.setText(id);
                jTextField8.setText(hasil.getString(2));
                jTextField9.setText(hasil.getString(3));
                jTextField10.setText(hasil.getString(4));
                jTextField7.setText(hasil.getString(5));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormLembaga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
