    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTextField7.getText().equals("") || jTextField8.getText().equals("") || jTextField9.getText().equals("") || jTextField10.getText().equals("") || jTextField6.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Pastikan tidak ada data yang kosong!");
        } else {
            try {
            PreparedStatement stmt = koneksiDB.prepareStatement("update pengaturan set batas_buku='" + jTextField8.getText() + "', batas_hari='" + jTextField9.getText() + "', denda_hari='" + jTextField10.getText() + "', denda_rusak='" + jTextField7.getText() + "' where id_pengaturan='" + jTextField6.getText() + "'");
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Simpan data berhasil!");
            FormMenuUtama.jLabel8.setText(jTextField8.getText());
            FormMenuUtama.jLabel12.setText(jTextField9.getText());
            FormMenuUtama.jLabel16.setText(jTextField10.getText());
            FormMenuUtama.jLabel14.setText(jTextField7.getText());
            kondisiAwal();
        } catch (HeadlessException | SQLException e) {
        }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

